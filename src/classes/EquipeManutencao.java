package classes;

public class EquipeManutencao {
	private int idEquipe;
	private String descricao;
	private int alocacaoMaxima;
	private int alocacaoMinima;
	private String sigla;
	private int idEspecialidade;
	private String dataCadastro;
	private int idFuncionario;
	
	public int getIdEquipe() {
		return idEquipe;
	}
	public void setIdEquipe(int idEquipe) {
		this.idEquipe = idEquipe;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public int getAlocacaoMaxima() {
		return alocacaoMaxima;
	}
	public void setAlocacaoMaxima(int alocacaoMaxima) {
		this.alocacaoMaxima = alocacaoMaxima;
	}
	public int getAlocacaoMinima() {
		return alocacaoMinima;
	}
	public void setAlocacaoMinima(int alocacaoMinima) {
		this.alocacaoMinima = alocacaoMinima;
	}
	public String getSigla() {
		return sigla;
	}
	public void setSigla(String sigla) {
		this.sigla = sigla;
	}
	public int getIdEspecialidade() {
		return idEspecialidade;
	}
	public void setIdEspecialidade(int idEspecialidade) {
		this.idEspecialidade = idEspecialidade;
	}
	public String getDataCadastro() {
		return dataCadastro;
	}
	public void setDataCadastro(String dataCadastro) {
		this.dataCadastro = dataCadastro;
	}
	public int getIdFuncionario() {
		return idFuncionario;
	}
	public void setIdFuncionario(int idFuncionario) {
		this.idFuncionario = idFuncionario;
	}
}
