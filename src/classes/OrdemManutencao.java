package classes;

public class OrdemManutencao {
	private int idOrdem;
	private String dataManutencao;
	private String dataOrdem;
	private int idTipo;
	private int idLocal;
	private int idEquipamento;
	private String tituloOrdem;
	private String descricaoServico;
	private int idCentro;
	private int idEquipe;
	
	public int getIdOrdem() {
		return idOrdem;
	}
	public void setIdOrdem(int idOrdem) {
		this.idOrdem = idOrdem;
	}
	public String getDataManutencao() {
		return dataManutencao;
	}
	public void setDataManutencao(String dataManutencao) {
		this.dataManutencao = dataManutencao;
	}
	public String getDataOrdem() {
		return dataOrdem;
	}
	public void setDataOrdem(String dataOrdem) {
		this.dataOrdem = dataOrdem;
	}
	public int getIdTipo() {
		return idTipo;
	}
	public void setIdTipo(int idTipo) {
		this.idTipo = idTipo;
	}
	public int getIdLocal() {
		return idLocal;
	}
	public void setIdLocal(int idLocal) {
		this.idLocal = idLocal;
	}
	public int getIdEquipamento() {
		return idEquipamento;
	}
	public void setIdEquipamento(int idEquipamento) {
		this.idEquipamento = idEquipamento;
	}
	public String getTituloOrdem() {
		return tituloOrdem;
	}
	public void setTituloOrdem(String tituloOrdem) {
		this.tituloOrdem = tituloOrdem;
	}
	public String getDescricaoServico() {
		return descricaoServico;
	}
	public void setDescricaoServico(String descricaoServico) {
		this.descricaoServico = descricaoServico;
	}
	public int getIdCentro() {
		return idCentro;
	}
	public void setIdCentro(int idCentro) {
		this.idCentro = idCentro;
	}
	public int getIdEquipe() {
		return idEquipe;
	}
	public void setIdEquipe(int idEquipe) {
		this.idEquipe = idEquipe;
	}
}
