package classes;

public class LocalInstalacao {
	private int idLocal;
	private String tagLocal;
	private String descricao;
	private int idEquipe;
	private String equipe;
	private int idCentroCusto;
	private String dataCadastro;
	private int idFuncionario;
	
	//Getters e Setters
	public int getIdLocal() {
		return idLocal;
	}
	public void setIdLocal(int idLocal) {
		this.idLocal = idLocal;
	}
	public String getTagLocal() {
		return tagLocal;
	}
	public void setTagLocal(String tagLocal) {
		this.tagLocal = tagLocal;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public int getIdEquipe() {
		return idEquipe;
	}
	public void setIdEquipe(int idEquipe) {
		this.idEquipe = idEquipe;
	}
	public int getIdCentroCusto() {
		return idCentroCusto;
	}
	public void setIdCentroCusto(int idCentroCusto) {
		this.idCentroCusto = idCentroCusto;
	}
	public String getDataCadastro() {
		return dataCadastro;
	}
	public void setDataCadastro(String dataCadastro) {
		this.dataCadastro = dataCadastro;
	}
	public int getIdFuncionario() {
		return idFuncionario;
	}
	public void setIdFuncionario(int idFuncionario) {
		this.idFuncionario = idFuncionario;
	}
	public String getEquipe() {
		return equipe;
	}
	public void setEquipe(String equipe) {
		this.equipe = equipe;
	}
}