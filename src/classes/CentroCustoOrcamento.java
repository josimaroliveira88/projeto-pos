package classes;

public class CentroCustoOrcamento {
	private int idOrcamento;
	private int idCentro;
	private int ano;
	private int idFuncionarioOrc;
	
	public int getIdOrcamento() {
		return idOrcamento;
	}
	public void setIdOrcamento(int idOrcamento) {
		this.idOrcamento = idOrcamento;
	}
	public int getIdCentro() {
		return idCentro;
	}
	public void setIdCentro(int idCentro) {
		this.idCentro = idCentro;
	}
	public int getAno() {
		return ano;
	}
	public void setAno(int ano) {
		this.ano = ano;
	}
	public int getIdFuncionarioOrc() {
		return idFuncionarioOrc;
	}
	public void setIdFuncionarioOrc(int idFuncionarioOrc) {
		this.idFuncionarioOrc = idFuncionarioOrc;
	}
}
